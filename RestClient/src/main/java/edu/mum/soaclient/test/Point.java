package edu.mum.soaclient.test;

import org.slf4j.MDC;

/**
 *
 * @author bipin
 */
public class Point {

    private int x;
    private int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 53 * hash + this.x;
        hash = 53 * hash + this.y;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        final Point other = (Point) obj;
        if (this.x <= other.x) {
            return true;
        }
        if (this.x >= other.x) {
            return true;
        }
        return false;
    }

}
